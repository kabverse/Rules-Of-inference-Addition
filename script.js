const aboutLink = document.querySelector('.about');
const aboutPop = document.querySelector('.about-pop');

// Show the about pop when hovering over the About link
aboutLink.addEventListener('mouseover', () => {
    aboutPop.style.opacity = '1'; // Make it visible
});

// Hide the about pop when not hovering over the About link
aboutLink.addEventListener('mouseout', () => {
    aboutPop.style.opacity = '0'; // Hide it again
});

